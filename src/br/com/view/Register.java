    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        if (txtName.getText().equals("") || txtEmail.getText().equals("") || txtCourse.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Fill the fields.");
        } else {
            Student student = new Student();
            StudentDAO studentDAO = new StudentDAO();

            student.setName(txtName.getText());
            student.setEmail(txtEmail.getText());
            student.setCourse(txtCourse.getText());

            try {
                if (studentDAO.validateEmail(student.getEmail())) {
                    studentDAO.insertStudent(student);
                    JOptionPane.showMessageDialog(null, "Student successfully inserted!");
                    cleanTextFields();
                } else {
                    JOptionPane.showMessageDialog(null, "This email is already registered in the system!");
                }      
            } catch (SQLException ex) {               
                Logger.getLogger(Register.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnRegisterActionPerformed

